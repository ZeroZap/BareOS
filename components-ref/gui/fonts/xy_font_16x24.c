/**
 * @brief Get character bitmap data
 */
const uint8_t* xy_font_16x24_get_char(uint8_t ch)
{
    if (ch < 0x20 || ch > 0x7E) {
        return NULL;
    }
    return (const uint8_t*)g_font_16x24_table[ch - 0x20];
}

/**
 * @brief Measure ASCII string width
 */
uint16_t xy_font_16x24_measure(const char *str)
{
    if (!str) return 0;

    uint16_t width = 0;
    while (*str) {
        if (*str >= 0x20 && *str <= 0x7E) {
            width += 16;
        }
        str++;
    }
    return width;
}
