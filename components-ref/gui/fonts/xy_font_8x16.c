{
    return &g_font_8x16;
}

/**
 * @brief Get character bitmap data
 */
const uint8_t* xy_font_8x16_get_char(uint8_t ch)
{
    if (ch < 0x20 || ch > 0x7E) {
        return NULL;
    }
    return g_font_8x16_table[ch - 0x20];
}

/**
 * @brief Measure ASCII string width
 */
uint16_t xy_font_8x16_measure(const char *str)
{
    if (!str) return 0;

    uint16_t width = 0;
    while (*str) {
        if (*str >= 0x20 && *str <= 0x7E) {
            width += 8;
        }
        str++;
    }
    return width;
}
